BATCH_SIZE        = 10
SLEEP_BETWEEN     = 2.2
GROQ_FALLBACK     = "Chưa rõ"
CHECKPOINT_FILE   = os.path.join(OUTPUT_DIR, "checkpoint.json")
TEMP_OUTPUT       = os.path.join(OUTPUT_DIR, "data_normalized_temp.csv")

client = Groq(api_key=GROQ_API_KEY)

def _safe_str(val, max_chars=400):
    return "" if pd.isna(val) else str(val).strip()[:max_chars]

def _build_prompt(rows):
    blocks = []
    for r in rows:
        field_lines = "\n".join(f'    - {c}: "{v}"' for c,v in r["fields"].items())
        blocks.append(
            f'--- ROW {r["row_id"]} ---\n'
            f'  Tiêu đề: {r["title"]}\n'
            f'  Mô tả  : {r["desc"]}\n'
            f'  Cần điền:\n{field_lines}'
        )
    return f"""Chuẩn hóa dữ liệu bất động sản. Dựa vào tiêu đề và mô tả để điền.
Nhãn hợp lệ:
- direction       : {VALID_DIRECTION}
- legal_status    : {VALID_LEGAL}
- furniture_status: {VALID_FURNITURE}
Nếu không đề cập → "{GROQ_FALLBACK}". Chỉ trả JSON array, không giải thích.
Format: [{{"row_id":<số>,"direction":<nhãn>,"legal_status":<nhãn>,"furniture_status":<nhãn>}}]
Dữ liệu:
{"".join(blocks)}"""

def _call_groq(rows):
    for attempt in range(3):
        try:
            resp = client.chat.completions.create(
                model=GROQ_MODEL,
                messages=[{"role":"user","content":_build_prompt(rows)}],
                temperature=0, max_tokens=1024,
            )
            raw = re.sub(r"^```json|^```|```$","", resp.choices[0].message.content.strip(), flags=re.MULTILINE).strip()
            return {r["row_id"]: r for r in json.loads(raw)}
        except Exception as e:
            print(f"   [attempt {attempt+1}/3]  {e}")
            time.sleep(2**attempt)
    return {}

