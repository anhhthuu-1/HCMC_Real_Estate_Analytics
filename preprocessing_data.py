def rule_direction(val):
    if pd.isna(val) or str(val).strip() == "":
        return None

    # Buoc 1: chuan hoa khoang trang va dau gan het
    v = str(val).strip()
    v = re.sub(r"\s*[-–−]\s*", "-", v)   # "Đông - Nam", "Đông– Nam" → "Đông-Nam"
    v = re.sub(r"\s+", "-", v)           # "Đông Nam" → "Đông-Nam"
    v = v.lower()

    DIRECTION_MAP = {
        # Đông-Nam (tat ca bien the sau khi chuan hoa deu thanh "đông-nam")
        "đông-nam"  : "Đông-Nam",
        "dong-nam"  : "Đông-Nam",
        "dn"        : "Đông-Nam",

        # Đông-Bắc
        "đông-bắc"  : "Đông-Bắc",
        "dong-bac"  : "Đông-Bắc",
        "db"        : "Đông-Bắc",

        # Tây-Nam
        "tây-nam"   : "Tây-Nam",
        "tay-nam"   : "Tây-Nam",
        "tn"        : "Tây-Nam",

        # Tây-Bắc
        "tây-bắc"   : "Tây-Bắc",
        "tay-bac"   : "Tây-Bắc",
        "tb"        : "Tây-Bắc",

        # Don
        "đông"      : "Đông",
        "dong"      : "Đông",
        "tây"       : "Tây",
        "tay"       : "Tây",
        "nam"       : "Nam",
        "bắc"       : "Bắc",
        "bac"       : "Bắc",
    }

    # Exact match
    if v in DIRECTION_MAP:
        return DIRECTION_MAP[v]

    # Contains match: dai → ngan
    for key in sorted(DIRECTION_MAP, key=len, reverse=True):
        if key in v:
            return DIRECTION_MAP[key]

    return None

